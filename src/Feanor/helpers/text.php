function postSlug ($str)
{
    return strtolower(
        preg_replace(
            array('/[^a-zA-Z0-9 -]/', '/[ -]+/', '/^-|-$/'),
            array('', '-', ''),
            removeAccent($str)
        )
    );
}

function humanize ($str)
{
    return ucwords(preg_replace('/[_]+/', ' ', strtolower(trim($str))));
}

/**
 * Funcion para cambiar las fechas y sean del tipo "Hace 2 meses"
 *
 * @param date $date
 * @return string Código HTML
 */
function nicetime ($date)
{
    if (empty($date)) {
        return "No se tiene la fecha";
    }
    $periods = array(
        "segundo", "minuto", "hora", "día", "semana", "mes", "año", "decada");
    $lengths = array(
        "60", "60", "24", "7", "4.35", "12", "10");
    $now = time();
    $unix_date = strtotime($date);
    // check validity of date
    if (empty($unix_date)) {
        return "Fecha corrupta";
    }
    // is it future date or past date
    if ($now > $unix_date) {
        $difference = $now - $unix_date;
        $tense = "Hace";
    } else {
        $difference = $unix_date - $now;
        $tense = "Falta";
    }
    for ($j = 0; $difference >= $lengths[$j] && $j < count($lengths) - 1; $j++) {
        $difference /= $lengths[$j];
    }
    $difference = round($difference);
    if ($difference != 1) {
        if ($periods[$j] == 'mes') {
            $periods[$j] .= "es";
        } else {
            $periods[$j] .= "s";
        }
    }
    return "<span class='fecha'><strong>$date</strong> {$tense}&nbsp;$difference&nbsp;$periods[$j] </span>";
}

/**
 * Parse the form attributes
 *
 * Helper function used by some of the form helpers
 *
 * @access	private
 * @param	array
 * @param	array
 * @return	string
 */
function parseFormAttributes ($attributes, $default)
{
    if (is_array($attributes)) {
        foreach ($default as $key => $val) {
            if (isset($attributes[$key])) {
                $default[$key] = $attributes[$key];
                unset($attributes[$key]);
            }
        }

        if (count($attributes) > 0) {
            $default = array_merge($default, $attributes);
        }
    }

    $att = '';

    foreach ($default as $key => $val) {
        if ($key == 'value') {
            $val = formPrep($val, $default['name']);
        }

        $att .= $key . '="' . $val . '" ';
    }

    return $att;
}

/**
 * Text Input Field
 *
 * @access	public
 * @param	mixed
 * @param	string
 * @param	string
 * @return	string
 */
function formInput ($data = '', $value = '', $extra = '')
{
    $defaults = array('type'=>'text', 'name'=>((!is_array($data)) ? $data : ''), 'value'=>$value);

    return "<input " . parseFormAttributes($data, $defaults) . $extra . " />";
}

/**
 * Form Prep
 *
 * Formats text so that it can be safely placed in a form field in the event it has HTML tags.
 *
 * @access	public
 * @param	string
 * @return	string
 */
function formPrep ($str = '', $field_name = '')
{
    $prepped_fields = array();

    // if the field name is an array we do this recursively
    if (is_array($str)) {
        foreach ($str as $key => $val) {
            $str[$key] = formPrep($val);
        }

        return $str;
    }

    if ($str === '') {
        return '';
    }

    // we've already prepped a field with this name
    // @todo need to figure out a way to namespace this so
    // that we know the *exact* field and not just one with
    // the same name
    if (isset($prepped_fields[$field_name])) {
        return $str;
    }

    $str = htmlspecialchars($str);

    // In case htmlspecialchars misses these.
    $str = str_replace(array("'", '"'), array("&#39;", "&quot;"), $str);

    if ($field_name != '') {
        $prepped_fields[$field_name] = $field_name;
    }

    return $str;
}
